public class RectangleComputation {

    public static void main(String[] args) {
        double area, perimeter, lenght, width;
        lenght = 5;
        width = 3;
        area = lenght * width;
        perimeter = 2 * (lenght + width);

        System.out.print(" The area of the rentangle is ");
        System.out.println(area);
        System.out.print(" The perimeter of the rentagle is ");
        System.out.println(perimeter);

    }
}
